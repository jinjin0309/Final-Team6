print("â€¢ t-test(ë‹¨ì¸¡) ìœ ì˜ ìŠ¤ëƒ…ìƒ·:", int(res_df["sig_t_one"].sum()))
print("â€¢ U-test(ë‹¨ì¸¡) ìœ ì˜ ìŠ¤ëƒ…ìƒ·:", int(res_df["sig_u_one"].sum()))

print("\nğŸ“Š ìŠˆí¼-ì¼ë°˜ í‰ê· ì°¨ í° ìƒìœ„ 10ê°œ:")
